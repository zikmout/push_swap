#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*s1;
	size_t	i;

	s1 = (char*)b;
	i = 0;
	while (len > i)
	{
		s1[i] = (char)c;
		i++;
	}
	return (b);
}
