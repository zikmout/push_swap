#include "libft.h"

size_t		ft_wcslen(wchar_t *str)
{
	size_t	len;

	len = 0;
	if (str)
	{
		while (*str)
		{
			len = len + ft_cslen(*str);
			str++;
		}
	}
	return (len);
}
