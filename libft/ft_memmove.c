#include "libft.h"

void	*ft_memmove(void *dest, void const *src, size_t n)
{
	char const	*s;
	char		*d;

	s = (char *)src;
	d = (char *)dest;
	if (s > d)
	{
		while (n--)
			*d++ = *s++;
	}
	else
	{
		d += n;
		s += n;
		while (n--)
			*--d = *--s;
	}
	return (dest);
}
